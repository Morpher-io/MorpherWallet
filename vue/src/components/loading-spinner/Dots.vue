</template>
<script lang="ts">
import Vue from 'vue';
import Component from 'vue-class-component';
import { Prop } from 'vue-property-decorator';

@Component
export default class Dots extends Vue {
	@Prop({ default: '40px' })
	size!: string;

	get innerStyles() {
		const size = parseInt(this.size);
		return {
			transform: 'scale(' + size / 60 + ')'
		};
	}

	get styles() {
		return {
			width: this.size,
			height: this.size
		};
	}
}
</script>
